#include "ft_printf.h"

void	putnbr(int n, int *len)
{
	if (n < 0)
	{
		if (n == -2147483648)
		{
			*len += 11;
			ft_putstr("2147483648");
			return ;
		}
		n = -n;
	}
	if (n > 9)
		putnbr(n / 10, len);
	*len += 1;
	ft_putchar((n % 10) + '0');
}
